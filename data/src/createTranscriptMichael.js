import React, { useState, useEffect } from 'react';

function App() {
  const [response, setResponse] = useState(null);
  const [error, setError] = useState(null);
  const [loading, setLoading] = useState(false);

  const transcriptText = `Michael is one of the founders of a company called Rilla, where he serves as the Chief AI Officer. Rilla provides speech analytics for outside sales, analyzing conversations to help sales reps sell more effectively. Michael got into AI somewhat unexpectedly - he was originally studying electrical engineering in college but was struggling academically. After watching the TV show Westworld, he became fascinated with AI and started teaching himself Python and machine learning concepts through online resources.

As a senior in college, Michael created a music recommendation system that used AI to analyze the actual content of songs, rather than just collaborative filtering. He entered this project into competitions and hackathons, which led to some exciting opportunities. At one event, he ended up presenting to high-profile judges like Jack Selby (PayPal co-founder) and even got connected with Apple Music executives.

Michael emphasized that he had no formal training in AI or computer science. He learned through grinding - watching tutorials, implementing papers, and working on projects. When starting Rilla, he spent two years solving the challenging problem of speaker diarization (determining who is speaking and when in an audio recording). Michael values creativity and out-of-the-box thinking when hiring, as the problems they tackle often have no existing solutions.

The video concluded with a demonstration of an AI-powered accountability coach that Headstarter is developing. It uses conversational AI to check in on users' job search progress, learning goals, and overall wellbeing. Michael was impressed by the speed and capabilities of the system, which the Headstarter team revealed is running on the newly released Llama 3 model.`;

  const postTranscript = async () => {
    setLoading(true);
    setError(null);

    try {
      const res = await fetch('https://jo589y2zh7.execute-api.us-east-1.amazonaws.com/test/transcriptions/transcript', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ transcriptText }),
      });

      if (!res.ok) {
        throw new Error(`HTTP error! Status: ${res.status}`);
      }

      const data = await res.json();
      setResponse(data);
    } catch (err) {
      console.error('Error posting data:', err);
      setError(err.message);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    postTranscript();
  }, []);

  return (
    <div style={{ maxWidth: '800px', margin: '0 auto', padding: '20px' }}>
      <h1 style={{ fontSize: '24px', marginBottom: '20px' }}>Rilla Voice: Speech Analytics for Outside Sales</h1>

      <div style={{ border: '1px solid #ccc', borderRadius: '4px', padding: '20px', marginBottom: '20px' }}>
        <h2 style={{ fontSize: '20px', marginBottom: '10px' }}>Transcript</h2>
        {transcriptText.split('\n\n').map((paragraph, index) => (
          <p key={index} style={{ marginBottom: '10px' }}>{paragraph.trim()}</p>
        ))}
      </div>

      <button 
        onClick={postTranscript} 
        disabled={loading}
        style={{
          backgroundColor: loading ? '#ccc' : '#007bff',
          color: 'white',
          padding: '10px 20px',
          border: 'none',
          borderRadius: '4px',
          cursor: loading ? 'not-allowed' : 'pointer',
          marginBottom: '20px'
        }}
      >
        {loading ? 'Posting...' : 'Posting Transcript'}
      </button>

      {loading && <p>Posting transcript...</p>}

      {error && (
        <div style={{ backgroundColor: '#ffeeee', border: '1px solid #ff0000', borderRadius: '4px', padding: '20px', marginBottom: '20px' }}>
          <h2 style={{ fontSize: '20px', marginBottom: '10px', color: '#ff0000' }}>Error</h2>
          <p style={{ color: '#ff0000' }}>{error}</p>
        </div>
      )}

      {response && (
        <div style={{ border: '1px solid #ccc', borderRadius: '4px', padding: '20px' }}>
          <h2 style={{ fontSize: '20px', marginBottom: '10px' }}>Posting Transcript</h2>
          <pre style={{ whiteSpace: 'pre-wrap', overflowX: 'auto' }}>
            {JSON.stringify(response, null, 2)}
          </pre>
        </div>
      )}
    </div>
  );
}

export default App;